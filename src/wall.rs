pub fn setup_walls(mut commands: Commands, asset_server: Res<AssetServer>) {
    for i in 0..=9 {
        commands.spawn((
            Sprite::from_image(asset_server.load("textures/wall.png")),
            Transform::from_xyz(-100., (i * 64) as f32, 0.),
            StaticCollider {
                shape: Aabb2d {
                    min: Vec2 { x: 0., y: 0. },
                    max: Vec2 { x: 64., y: 64. },
                },
            },
        ));
    }
}

